# 1.赋值运算符：=，*=，-=，*=，/=，%=，//=,**=
"""
所有赋值运算符左边必须是变量；组合的赋值运算符左边的变量必须已经赋值
所有的赋值运算符最终都会进行赋值的操作
"""
num =100 #直接将右边的结果赋给左边的变量
